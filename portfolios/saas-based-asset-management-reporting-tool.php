						 <p>The proposed web application has been developed overcoming MS Access and Excel sheets turning into a unique reporting tool strives on providing the overview of the plant health with a goal of prolonged equipment life thus avoiding any unexpected breakdowns, costly downtime along with measuring the performance of the machinery installed.</p>
						 <p>The main purpose of this reporting tools has been to show the status of a list of machines, ie what the alarm status is – Green / Amber / Red, as determined by their readings ( these alarms are setup in other data collection software,  SQL based application that generates a report with what alarm each asset is in ). </p>
						 <h3>USP/Features</h3>
						 <ul>
							<li>  “Reporter Wizard” used by Admin to create a report for all machines showing each alarm status, the site identifies those machines in alarm and gives fields for the admins to type up the analysis. This analysis is shown in the site for each machine in alarm with a historical graph of each individual reading. </li>
							<li>  The application offers  “ message “ board capability where machine notes are stored and various emails sent to the Standard and Admin users.  </li>
							<li>  Supporting the reporting tool,  a different program has been developed to be used by the internal staff members which produces a list of the machines that are above pre-set alarm levels for a specific date range. </li>
