							  <li>Due to the current prevailing situation the market is down, the purchasing power of the individuals has declined therefore the demo for the products is low.</li>
							  <li>There are only a few buyers in the market so to ensure that they don&#39;t get dissatisfied we need to use effective CRM.</li>
							  <li>This is the right time to show empathy towards your customers.</li>
