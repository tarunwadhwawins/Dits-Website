							  <li>Be calm, make sound decisions, and execute your marketing strategies.</li>
							  <li>All are looking for some normalcy in these hectic times, give that to your customer.</li>
							  <li>Redistribute budgets and prioritize short term goals and long term goals.</li>
							  <li>COVID-19 has disturbed the business operations drastically. Our government has effectively managed to keep in mind the current situation taking care of both the health and safety of its people and business.</li>
							  <li>Don&#39;t panic but breathe- it&#39;s not necessary to reach out to every customer mentioned in the database but make sound decisions, be calm, and execute your marketing strategies.</li>
							  <li>COVID-19 is a serious disease and smart companies have taken and are taking all mandatory steps to ensure the utmost safety of their employees. Sales need to happen, also work needs to be done, opportunities need to be pursued, so overall whether employees working from home or office collaboration is mandatory, that&#39;s where CRM( Customer Relationship Management) comes in picture.</li>
							  <li>For years CRM software was under-utilized, but now all thanks to COVID-19 <a href="https://en.wikipedia.org/wiki/Customer_relationship_management" target="_blank">CRM software</a> is utilized in full force to ensure sales are done, work is happening appropriately, opportunities are pursued and employees are working anywhere with great collaboration.</li>
