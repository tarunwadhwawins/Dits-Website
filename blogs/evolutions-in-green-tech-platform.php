                     <p>Today’s businesses know they aren’t just striving for the now, they’re reinvesting in the tomorrow. As environment change and other environmental problems take mid-stage and influence all domains of our experiences, many responsible firms are initiating to diminish their carbon traces. Too small steps can have a huge environmental impression. The consent of environment specialists is quite transparent. The realm defies an instant CO2 predicament. The carbon in our environment has formed a sheath of fumes that catches flame and is altering the world’s environment.</p>
                     <h3>How Firms Are Planning To Go Carbon Neutral In Future</h3>
					                      <img src="../assets/images/how-firms-are-planning-to-go-carbon-neutral-in-future.jpg" title="" alt="" class="img-fluid">

                     <p>Many firms are enthusiastic about lessening their carbon traces and setting standards for others in their businesses. These organizations adopt innovative explications and determined aims to witness real returns. Many companies are planning to be carbon neutral by the end of 2020, by lessening and neutralizing its carbon emissions. The pledge reflects these firms to concentrate on growing as more sustainable businesses by enhancing environmental production and driving long-term, sustainable extension. In order to accomplish carbon neutrality, these companies will focus on regions including lessening riding effusions, sustainable acquirement habits and buying more renewable energy and green-tech platforms to power factories and office premises.</p>
                     <h3>Agriculture</h3>
                                          <img src="../assets/images/Agriculture.jpg" title="" alt="" class="img-fluid"/>

                     <p>1. <b>Land O’Lakes</b>. The large co-op provides resources to agriculture startups and gives them resources to follow eco-friendly farming practices.</p>
                     <p>2. <b>John Deere</b>. The farming equipment company aims to recycle 85% of materials and reduce carbon emissions on 90% of new products to create sustainable inventory.</p>
                     <p>3. <b>Coca-Cola</b>. The soda company is fiercely dedicated to sustainable agriculture and is working towards sustainably sourcing all key ingredients by 2020 and reducing its carbon footprint by 25% by 2025.</p>
                     <h3>Automotive</h3>
                                          <img src="../assets/images/Automotive.jpg" title="" alt="" class="img-fluid"/>

                     <p>1. <b>BMW</b>. The BMW Group is regularly named the world’s most sustainable car manufacturer for its efforts to create fuel-saving and alternative cars with clean production processes.</p>
                     <p>2. <b>Valeo</b>. Ranked as the 5th most sustainable French company in any industry, Valeo has invested millions in sustainability and creates innovative solutions to lower its cars’ carbon emissions.</p>
                     <p>3. <b>Honda</b>. Honda is ranked as one of the most fuel-efficient auto manufacturers in the U.S. because of its innovative practices to cut carbon emissions and develop a hydrogen fuel-powered vehicle.</p>
                     <h3>Hospitality/Restaurants</h3>
                                          <img src="../assets/images/Hospitality-Restaurants.jpg" title="" alt="" class="img-fluid"/>

                     <p>1. <b>Disney</b>. At all of its parks, Disney uses zero net direct greenhouse gas emissions and has a zero-waste policy so that nothing ends up in landfills.</p>
                     <p>2. <b>Starbucks</b>. The coffee giant aims to be free of plastic straws by 2020 by redesigning its cold drink cups with a strawless lid that is being rolled out in the U.S. and Canada.</p>
                     <p>3. <b>McDonald’s</b>. Switching to energy-efficient appliances at its restaurants cut energy waste by 25%; it also plans to source all of its packagings from recycled materials by 2025.</p>
                     <h3>Retail</h3>
                                          <img src="../assets/images/Retail.jpg" title="" alt="" class="img-fluid"/>

                     <p>1. <b>Levi Strauss & Co.</b> Levi’s is fighting the fashion industry’s notorious waste with a line of jeans made of recycled water bottles as well as partnerships with other eco-friendly clothing brands.</p>
                     <p>2. <b>Walmart</b>. Walmart stores use 100% renewable energy sources, and the company even cut ties with suppliers that had manufacturing or distribution practices that added to carbon emissions.</p>
                     <p>3. <b>Brooks</b>. The running shoe company created a completely biodegradable shoe that is both durable and eco-friendly.</p>
                     
